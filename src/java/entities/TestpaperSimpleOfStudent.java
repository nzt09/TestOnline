package entities;
public class TestpaperSimpleOfStudent {
	private String courseName;
	private String testTime;
	private int score;
	private int passedNum;
	private int unpassedNum;
	private String advice;
	private String studentNum;
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getTestTime() {
		return testTime;
	}
	public void setTestTime(String testTime) {
		this.testTime = testTime;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public int getPassedNum() {
		return passedNum;
	}
	public void setPassedNum(int passedNum) {
		this.passedNum = passedNum;
	}
	public int getUnpassedNum() {
		return unpassedNum;
	}
	public void setUnpassedNum(int unpassedNum) {
		this.unpassedNum = unpassedNum;
	}
	public String getAdvice() {
		return advice;
	}
	public void setAdvice(String advice) {
		this.advice = advice;
	}
	public String getStudentNum() {
		return studentNum;
	}
	public void setStudentNum(String studentNum) {
		this.studentNum = studentNum;
	}
	
}
